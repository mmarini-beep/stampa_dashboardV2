export type TranslationKey = keyof typeof translations.es

export function createT(lang: Lang) {
  return function t(key: TranslationKey): string {
    const tr = translations[lang] as Record<string, unknown>
    const fallback = translations.en as Record<string, unknown>
    const val = tr[key] ?? fallback[key]
    if (Array.isArray(val)) return val.join(', ')
    return (val as string) ?? String(key)
  }
}

// ── React context ─────────────────────────────────────────────────────────────
export const LangContext = createContext<(key: TranslationKey) => string>(() => '')
export function useLang() { return useContext(LangContext) }